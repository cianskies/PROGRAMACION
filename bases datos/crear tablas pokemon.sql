drop table pokemon;
drop table especie_pokemon;
drop table multiplicador;
drop table ataque;
drop table tipo;


create table tipo (
	id_tipo number(2) primary key,
	nombre varchar2(9) not null
);

create table ataque(
	id_ataque number(3) primary key,
	nombre varchar2(30) not null,
	rapido number(1) not null check (rapido in (0,1)),
	id_tipo references tipo,
	danho number(3) check (danho>=0),
	dps number(3,1) check (dps>=0)
);

create table multiplicador(
	id_tipo_ataque references tipo(id_tipo),
	id_tipo_defensa references tipo(id_tipo),
	valor number(3,2) default 1,
	constraint pk_multiplicador primary key (id_tipo_ataque, id_tipo_defensa)
);

create table especie_pokemon(
	id_pokedex number(3) primary key,
	nombre varchar2(20) unique not null,
	ataque number(3) not null,
	defensa number(3) not null,
	ataque_sp number(3) not null,
	defensa_sp number(3) not null,
	hp number(3) not null,
	velocidad number(3) not null,
	tipo1 references tipo(id_tipo) not null,
	tipo2 references tipo(id_tipo),
	generacion number(1) not null,
	legendario number(1) not null check (legendario in (0,1)),
	evoluciona_de references especie_pokemon(id_pokedex)
);


create table pokemon(
	id number(10) primary key,
	nombre varchar2(25) not null,
	id_pokedex references especie_pokemon not null,
	ataque_rapido references ataque(id_ataque) not null,
	ataque_cargado references ataque(id_ataque) not null,
	fecha_captura date not null,
	municipio_captura varchar2(30) not null
);




